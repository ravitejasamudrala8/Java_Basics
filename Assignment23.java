//23.Write a program using different math class methods
package assignments;

public class Assignment23 
{
	public static void main(String[] args) 
	{
		System.out.println(Math.addExact(10, 20));
		System.out.println(Math.subtractExact(30, 20));
		System.out.println(Math.multiplyExact(10, 20));
		System.out.println(Math.divideExact(10, 2));
		System.out.println(Math.min(20, 10));
		System.out.println(Math.max(20, 10));
		System.out.println(Math.PI);
		System.out.println(Math.abs(-23));
		System.out.println(Math.random());
		
	}

}
